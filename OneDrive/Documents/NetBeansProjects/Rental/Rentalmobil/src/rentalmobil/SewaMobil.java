 public void hitungselisih() {
         try {
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String strDate1 = df.format(jDatetglpeminjaman1.getDate());
            String strDate2 = df.format(jDatetglpengembalian.getDate());
            Date Tanggal1 = df.parse(strDate1);
            Date Tanggal2 = df.parse(strDate2);
            long Hari1 = Tanggal1.getTime();
            long Hari2 = Tanggal2.getTime();
            long diff = Hari2 - Hari1;
            long Lama = diff / (24 * 60 * 60 * 1000);
            String Hasil = (Long.toString(Lama));
            txt_lamaPinjam.setText(Hasil);
            txt_lamaSupir.setText(Hasil);

            int harga_sewa = Integer.parseInt(lb_Harga.getText()); // lbl_harga di pars ke integer
            int lama_sewa = Integer.parseInt(txt_lamaPinjam.getText());   // txt_lama di pars ke integer
            int harga_supir = Integer.parseInt(txt_lamaSupir.getText());
            int Total = (harga_sewa * lama_sewa) + (100000 * harga_supir) ;                     // mencari total Harga
            String a = Integer.toString(Total);
            lb_total.setText(a);



        } catch (Exception a) {
            JOptionPane.showMessageDialog(this, "Masukan Tanggal Peminjaman dan Tanggal Pengembalian");
        }
    }
 
